#Inicio Declaração de uma Matriz

matriz = [['A', 'B', 'C', 'D'],
    ['E', 'F', 'G', 'H'],
    ['i', 'j', 'k', 'l']]

print ('impressçao de todos os vetor')
print("Matriz =", matriz)

print ('Impressçao de cada vetor pertencente a matriz')
print ("vetor[0] =", matriz[0])
print ("vetor[1] =", matriz[1])
print ("vetor[2] =", matriz[2])

print ('Impressão de uma posições especificas de uma matriz')
print ("posicao[1][2] =", matriz[1][2])
print ("posicao[0][-1] =", matriz[0][-1])